package com.pantxi.calculator;

public class Calculator {

    // Méthode pour additionner deux nombres
    public  static int add(int opG, int opD) {
        return opG + opD;
    }

    // Méthode pour diviser deux nombres
    public  static int divide(int opG, int opD) {
        return opG / opD;
    }



}
